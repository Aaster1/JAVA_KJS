package Day02;

public class Ex19_GuguAll {
	public static void main(String[] args) {
		for (int i=1;i<=9;i++) {
			for(int j=1; j<=9; j++) {
				System.out.print(i+"x"+j+"="+i*j+"\t");
				
//				(단) x(1~9)
//				단에 대한 반복 : 1~9
//				각 단에 곱 : 1~9
				
//				A x B
//				i = 단 
//				j = 단에 대한 곱
			}
			System.out.print("\n");
		}
	}
}
